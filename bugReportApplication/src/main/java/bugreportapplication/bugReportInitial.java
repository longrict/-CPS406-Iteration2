    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // when viewBtn is clicked -> open up details about the bug itself
        // if not selected then inform user to select button, otherwise you can do it 
        if (unresolvedBugs.getSelectedIndex() == -1 && resolvedBugs.getSelectedIndex() == -1) {
            userMsg.setForeground(Color.red);
            userMsg.setText("Select a bug before clicking the button");
        // if unresolved bug
        } else if (!(unresolvedBugs.getSelectedIndex() == -1)) {
            dispose();
            bugReportView frame = new bugReportView(unresolvedBugReports.get(unresolvedBugs.getSelectedIndex()));
            frame.setVisible(true);
        } else {
            dispose();
            bugReportView frame = new bugReportView(resolvedBugReports.get(resolvedBugs.getSelectedIndex()));
            frame.setVisible(true);
        }
        
    }//GEN-LAST:event_viewBtnActionPerformed

    private void removeBugDB(BugReport bug) {
        AccessDB.deleteReport(bug.getTitle(), bug.getDesc(), bug.getPriority());
    }
    
    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        // Remove a bug first in local arrayList
        if (unresolvedBugs.getSelectedIndex() == -1 && resolvedBugs.getSelectedIndex() == -1) {
            userMsg.setForeground(Color.red);
            userMsg.setText("Select a bug before clicking the button");
        } else if (!(unresolvedBugs.getSelectedIndex() == -1)) {
            // Remove element from list and update
            BugReport removed = unresolvedBugReports.remove(unresolvedBugs.getSelectedIndex());
            removeBugDB(removed);
            unresolvedModel.removeElement(removed.getTitle());
            this.unresolvedBugs.setModel(unresolvedModel);
            userMsg.setForeground(new Color(0,204,51));
            userMsg.setText(removed.getTitle() + " has been successfully removed.");
        } else {
            // Remove element from list and update
            BugReport removed = resolvedBugReports.remove(resolvedBugs.getSelectedIndex());
            removeBugDB(removed);
            resolvedModel.removeElement(removed.getTitle());
            this.resolvedBugs.setModel(resolvedModel);
            userMsg.setForeground(new Color(0,204,51));
            userMsg.setText(removed.getTitle() + " has been successfully removed.");
        }
    }//GEN-LAST:event_removeBtnActionPerformed

