    public bugReportInitial() {
        initComponents();
        // manually add all bugs in the database
        ArrayList<BugReport> bugs = AccessDB.getReports();
        unresolvedBugReports = new ArrayList<BugReport>();
        resolvedBugReports = new ArrayList<BugReport>();
        resolvedModel = new DefaultListModel<>();
        unresolvedModel = new DefaultListModel<>();
                
        for (BugReport bug : bugs) {
            if (bug.getStatus().equals("Unresolved")) {
                unresolvedBugReports.add(bug);
                unresolvedModel.addElement(bug.getTitle());
            } else {
                resolvedBugReports.add(bug);
                resolvedModel.addElement(bug.getTitle());
            }
        }
        
        this.unresolvedBugs.setModel(unresolvedModel);
        this.resolvedBugs.setModel(resolvedModel);
        
        
        MouseListener mouseListenerResolved = new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int pos = resolvedBugs.locationToIndex(e.getPoint());
                resolvedBugs.setSelectedIndex(pos);
            }
        };
    
        MouseListener mouseListenerUnresolved = new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int pos = unresolvedBugs.locationToIndex(e.getPoint());
                unresolvedBugs.setSelectedIndex(pos);
            }
        };
    
    
    resolvedBugs.addMouseListener(mouseListenerResolved);
    unresolvedBugs.addMouseListener(mouseListenerUnresolved);
        
                
        // Set frame to be visible
        this.setVisible(true);
        
    }

